const server = require('./src/app.js');
const { conn, Diet } = require('./src/db.js');

const dietTypesDb = [
  'Gluten Free',
  'Ketogenic',
  'Vegetarian',
  'Lacto-Vegetarian',
  'Ovo-Vegetarian',
  'Vegan',
  'Pescetarian',
  'Paleo',
  'Primal',
  'low FODMAP',
  'Whole30',
];

// Syncing all the models at once.
conn
  .sync({ force: false })
  .then(() => {
    server.listen(3050, () => {
      console.log('%s listening at 3050'); // eslint-disable-line no-console
    });
  })
  .then(() =>
    dietTypesDb.map((e) => Diet.findOrCreate({ where: { name: e } }))
  );
